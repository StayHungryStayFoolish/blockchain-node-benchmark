#!/usr/bin/env bash
# config/csv_schema_registry.sh
# CSV Schema Registry for CSV writer fields.
#
# Keep this registry exactly symmetric with utils/csv_schema_registry.py.
# This follows the same bash/python registry pattern used by the network fields.
#
# Responsibilities:
#   csv_registry_disk_logical_names      -> list disk logical fields in CSV order
#   csv_registry_resolve <logical> <provider> <device_prefix> -> resolve physical column name
#   csv_registry_disk_header <device_prefix> <provider>       -> build the full disk header
#
# provider_aware fields are routed through disk_field_prefix:
#   aws/gcp/other currently share normalized physical names for the same logical metrics.
#   Provider identity is carried by the CSV cloud_provider column.
#   This matches config/providers/{aws,gcp,other}_provider.sh get_disk_field_prefix.
#
# Current scope: basic fields and disk fields. Other sections return empty until registered.

# Section order. This is the CSV segment order used by writers and readers.
# Keep it exactly symmetric with utils/csv_schema_registry.py SEGMENT_ORDER.
# Dynamic sections have runtime-dependent widths and are generated by their own helpers.
_CSV_REGISTRY_SEGMENT_ORDER="basic device network ena overhead block qps cgroup meta"
# Dynamic segments. Keep this symmetric with the Python DYNAMIC_SEGMENTS value.
_CSV_REGISTRY_DYNAMIC_SEGMENTS="device ena"

# Basic section fields. All physical names are fixed and match logical names.
# Keep this order aligned with utils/csv_schema_registry.py _BASIC_FIELDS.
_CSV_REGISTRY_BASIC_LOGICAL=(
    timestamp
    cpu_usage
    cpu_usr
    cpu_sys
    cpu_iowait
    cpu_soft
    cpu_idle
    mem_used
    mem_total
    mem_usage
)

# Disk section fields. Keep this order aligned with utils/csv_schema_registry.py _DISK_FIELDS.
_CSV_REGISTRY_DISK_LOGICAL=(
    disk_r_s
    disk_w_s
    disk_rkb_s
    disk_wkb_s
    disk_r_await
    disk_w_await
    disk_avg_await
    disk_aqu_sz
    disk_util
    disk_rrqm_s
    disk_wrqm_s
    disk_rrqm_pct
    disk_wrqm_pct
    disk_rareq_sz
    disk_wareq_sz
    disk_total_iops
    disk_iops_provider_adjusted
    disk_read_throughput_mibs
    disk_write_throughput_mibs
    disk_total_throughput_mibs
    disk_throughput_provider_adjusted
)

# Block/sync-health section fields, excluding the leading timestamp column.
_CSV_REGISTRY_BLOCK_LOGICAL=(
    local_block_height
    mainnet_block_height
    block_height_diff
    local_health
    mainnet_health
    data_loss
    sync_mode
    sync_status
    lag_value
    lag_unit
    freshness_gap_seconds
    probe_error
)

_CSV_REGISTRY_BLOCK_CACHE_REQUIRED=(
    timestamp
    local_block_height
    mainnet_block_height
    sync_mode
    sync_status
)

# Provider-aware logical field names.
_CSV_REGISTRY_PROVIDER_AWARE="disk_iops_provider_adjusted disk_throughput_provider_adjusted"

# List disk logical names in CSV order.
csv_registry_disk_logical_names() {
    echo "${_CSV_REGISTRY_DISK_LOGICAL[*]}"
}

# List basic logical names in CSV order.
csv_registry_basic_logical_names() {
    echo "${_CSV_REGISTRY_BASIC_LOGICAL[*]}"
}

# List all registered static logical names in segment order.
# Keep this symmetric with utils/csv_schema_registry.py CSVSchemaRegistry.all_logical_names().
csv_registry_all_logical_names() {
    echo "${_CSV_REGISTRY_BASIC_LOGICAL[*]} ${_CSV_REGISTRY_DISK_LOGICAL[*]} ${_CSV_REGISTRY_BLOCK_LOGICAL[*]}"
}

# List logical names for a registered static segment.
# Dynamic segments have no static enumeration and return empty.
# Unregistered static sections return empty.
csv_registry_segment_logical_names() {
    case "$1" in
        basic) echo "${_CSV_REGISTRY_BASIC_LOGICAL[*]}" ;;
        device) echo "${_CSV_REGISTRY_DISK_LOGICAL[*]}" ;;
        block) echo "${_CSV_REGISTRY_BLOCK_LOGICAL[*]}" ;;
        *) echo "" ;;
    esac
}

csv_registry_block_logical_names() {
    echo "${_CSV_REGISTRY_BLOCK_LOGICAL[*]}"
}

csv_registry_block_cache_required_keys() {
    echo "${_CSV_REGISTRY_BLOCK_CACHE_REQUIRED[*]}"
}

csv_registry_block_data_field_range() {
    local start=2
    local end=$((start + ${#_CSV_REGISTRY_BLOCK_LOGICAL[@]} - 1))
    echo "${start}-${end}"
}

# List provider-aware logical names.
csv_registry_provider_aware_names() {
    echo "$_CSV_REGISTRY_PROVIDER_AWARE"
}

# disk_field_prefix — provider-neutral naming: all providers use "normalized".
# Physical column names express logical metrics; provider comes from CSV cloud_provider.
# The provider argument is retained for future provider-specific naming.
# Keep this symmetric with utils/csv_schema_registry.py DISK_FIELD_PREFIX.
_csv_registry_disk_field_prefix() {
    case "$1" in
        aws)   echo "normalized" ;;
        gcp)   echo "normalized" ;;
        *)     echo "normalized" ;;
    esac
}

# Resolve a logical field name to a physical CSV column name.
# Usage: csv_registry_resolve <logical_name> <provider> <device_prefix>
# Unknown logical names return non-zero and emit an error.
csv_registry_resolve() {
    local logical="$1" provider="$2" prefix="$3"
    local dfp
    dfp="$(_csv_registry_disk_field_prefix "$provider")"
    case "$logical" in
        # Basic section fields. Physical names match logical names.
        timestamp)                         echo "timestamp" ;;
        cpu_usage)                         echo "cpu_usage" ;;
        cpu_usr)                           echo "cpu_usr" ;;
        cpu_sys)                           echo "cpu_sys" ;;
        cpu_iowait)                        echo "cpu_iowait" ;;
        cpu_soft)                          echo "cpu_soft" ;;
        cpu_idle)                          echo "cpu_idle" ;;
        mem_used)                          echo "mem_used" ;;
        mem_total)                         echo "mem_total" ;;
        mem_usage)                         echo "mem_usage" ;;
        # Disk section fields.
        disk_r_s)                          echo "${prefix}_r_s" ;;
        disk_w_s)                          echo "${prefix}_w_s" ;;
        disk_rkb_s)                        echo "${prefix}_rkb_s" ;;
        disk_wkb_s)                        echo "${prefix}_wkb_s" ;;
        disk_r_await)                      echo "${prefix}_r_await" ;;
        disk_w_await)                      echo "${prefix}_w_await" ;;
        disk_avg_await)                    echo "${prefix}_avg_await" ;;
        disk_aqu_sz)                       echo "${prefix}_aqu_sz" ;;
        disk_util)                         echo "${prefix}_util" ;;
        disk_rrqm_s)                       echo "${prefix}_rrqm_s" ;;
        disk_wrqm_s)                       echo "${prefix}_wrqm_s" ;;
        disk_rrqm_pct)                     echo "${prefix}_rrqm_pct" ;;
        disk_wrqm_pct)                     echo "${prefix}_wrqm_pct" ;;
        disk_rareq_sz)                     echo "${prefix}_rareq_sz" ;;
        disk_wareq_sz)                     echo "${prefix}_wareq_sz" ;;
        disk_total_iops)                   echo "${prefix}_total_iops" ;;
        disk_iops_provider_adjusted)       echo "${prefix}_${dfp}_iops" ;;
        disk_read_throughput_mibs)         echo "${prefix}_read_throughput_mibs" ;;
        disk_write_throughput_mibs)        echo "${prefix}_write_throughput_mibs" ;;
        disk_total_throughput_mibs)        echo "${prefix}_total_throughput_mibs" ;;
        disk_throughput_provider_adjusted) echo "${prefix}_${dfp}_throughput_mibs" ;;
        # Block/sync-health section fields. Physical names match logical names.
        local_block_height)                echo "local_block_height" ;;
        mainnet_block_height)              echo "mainnet_block_height" ;;
        block_height_diff)                 echo "block_height_diff" ;;
        local_health)                      echo "local_health" ;;
        mainnet_health)                    echo "mainnet_health" ;;
        data_loss)                         echo "data_loss" ;;
        sync_mode)                         echo "sync_mode" ;;
        sync_status)                       echo "sync_status" ;;
        lag_value)                         echo "lag_value" ;;
        lag_unit)                          echo "lag_unit" ;;
        freshness_gap_seconds)             echo "freshness_gap_seconds" ;;
        probe_error)                       echo "probe_error" ;;
        *)
            echo "csv_registry_resolve: unknown logical field: $logical" >&2
            return 1
            ;;
    esac
}

# Generate the full disk section header.
# Usage: csv_registry_disk_header <device_prefix> <provider>
csv_registry_disk_header() {
    local prefix="$1" provider="$2"
    local out="" logical phys
    for logical in "${_CSV_REGISTRY_DISK_LOGICAL[@]}"; do
        phys="$(csv_registry_resolve "$logical" "$provider" "$prefix")" || return 1
        if [[ -z "$out" ]]; then
            out="$phys"
        else
            out="${out},${phys}"
        fi
    done
    echo "$out"
}

# Generate the basic section header. Provider is ignored because names are fixed.
csv_registry_basic_header() {
    local out="" logical phys
    for logical in "${_CSV_REGISTRY_BASIC_LOGICAL[@]}"; do
        phys="$(csv_registry_resolve "$logical" "other" "")" || return 1
        if [[ -z "$out" ]]; then
            out="$phys"
        else
            out="${out},${phys}"
        fi
    done
    echo "$out"
}

csv_registry_block_header() {
    csv_registry_segment_header "block"
}

csv_registry_block_csv_header() {
    echo "timestamp,$(csv_registry_block_header)"
}

# Generate a registered static segment header.
# Usage: csv_registry_segment_header <segment> [provider] [device_prefix]
# provider/device_prefix are relevant only for provider-aware fields.
# Dynamic or unregistered segments return an empty string.
csv_registry_segment_header() {
    local segment="$1" provider="${2:-other}" prefix="${3:-}"
    local names out="" logical phys
    names="$(csv_registry_segment_logical_names "$segment")"
    [[ -z "$names" ]] && { echo ""; return 0; }
    for logical in $names; do
        phys="$(csv_registry_resolve "$logical" "$provider" "$prefix")" || return 1
        if [[ -z "$out" ]]; then
            out="$phys"
        else
            out="${out},${phys}"
        fi
    done
    echo "$out"
}
