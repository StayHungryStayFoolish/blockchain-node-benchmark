"""CSV Schema Registry for CSV reader fields.

This extends the bash/python registry pattern used by network fields to the
full monitoring CSV layout.

Responsibilities:
1. Define each logical field: {logical_name, semantic_type, segment, provider_aware, physical_name_template}
2. Resolve logical fields to physical CSV column names for readers.
3. Validate core invariants such as timestamp first, segment order, and column count.

Provider-aware fields use stable logical names while their physical CSV names
can vary through provider rules. Readers should resolve logical names through
this registry instead of hard-coding physical column names.

Current scope: core sections such as disk, basic, and block are registered.
Dynamic sections are generated by their collectors. The bash implementation in
config/csv_schema_registry.sh must remain symmetric with this file.
"""
from __future__ import annotations
from typing import Dict, List, Optional, Iterable
from dataclasses import dataclass


# disk_field_prefix values. All providers currently use "normalized" physical
# names for provider-adjusted disk metrics. Provider identity is carried by the
# separate cloud_provider column, while the provider argument is retained for
# future provider-specific naming if needed. AWS/GCP numeric differences are
# handled by disk_converter before values are written to CSV.
DISK_FIELD_PREFIX = {
    "aws": "normalized",
    "gcp": "normalized",
    "other": "normalized",
}

VALID_SEMANTIC_TYPES = {
    "rate", "throughput", "iops", "latency", "queue", "utilization",
    "ratio", "io_size", "iops_provider_adjusted", "throughput_provider_adjusted",
    "timestamp", "gauge", "unknown",
}

VALID_SEGMENTS = {
    "basic", "device", "network", "ena", "overhead",
    "block", "qps", "cgroup", "meta",
}


@dataclass(frozen=True)
class FieldDef:
    """Logical CSV field definition.

    logical_name: stable logical name used by readers.
    semantic_type: semantic category used by group_by_semantic.
    segment: CSV section name.
    provider_aware: whether the physical name is provider-dependent.
    physical_template: physical column template.
    """
    logical_name: str
    semantic_type: str
    segment: str
    provider_aware: bool
    physical_template: str


